def render(event_type: str, **kwargs) -> dict | None:
    """
    Pick a random template for event_type, format it with kwargs,
    and return {"title": str, "body": str} or None if unknown type.
    """
    templates = _TEMPLATES.get(event_type)
    if not templates:
        return None

    title_tpl, body_tpl = random.choice(templates)
    try:
        return {
            "title": title_tpl.format(**kwargs),
            "body":  body_tpl.format(**kwargs),
        }
    except KeyError:
        # Fallback: return unformatted if a placeholder is missing
        return {
            "title": title_tpl,
            "body":  body_tpl,
        }
