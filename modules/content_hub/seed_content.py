def seed_default_content():
    """
    Inserts default content into each table only if the table is empty.
    Uses INSERT OR IGNORE with a UNIQUE constraint on content to prevent
    duplicates even if called multiple times.
    Safe to call on every startup.
    """
    create_tables()
    conn = _get_conn()
    cur  = conn.cursor()

    for table, items in _SEED_MAP.items():
        if count_rows(table) > 0:
            continue  # already has content — skip
        rows = [(item.strip(),) for item in items]
        cur.executemany(
            f"INSERT OR IGNORE INTO {table} (content) VALUES (?)",
            rows
        )

    conn.commit()
