def handle_features_command(message) -> bool:
    text = (message.text or "").strip()
    if text not in ("مميزات بيلو", "مميزات البوت", "مساعدة"):
        return False
    uid = message.from_user.id
    cid = message.chat.id
    _send_main_menu(cid, uid, reply_to=message.message_id)
    return True


def _send_main_menu(cid, uid, reply_to=None, call=None):
    owner = (uid, cid)
    text  = (
        f"✨ <b>مرحباً بك في بيلو!</b>\n"
        f"{get_lines()}\n\n"
        "اختر قسماً لتعرف كيف يعمل البوت:"
    )
    buttons = [
        btn(f"{v['emoji']} {v['title']}", "feat_section",
            {"sec": k, "p": 0}, owner=owner, color=_B)
        for k, v in _SECTIONS.items()
    ]
    buttons.append(btn("❌ إغلاق", "feat_close", {}, owner=owner, color=_RD))

    layout = [2, 2, 1]

    if call:
        edit_ui(call, text=text, buttons=buttons, layout=layout)
    else:
        send_ui(cid, text=text, buttons=buttons, layout=layout,
                owner_id=uid, reply_to=reply_to)


@register_action("feat_section")
def on_section(call, data):
    uid  = call.from_user.id
    cid  = call.message.chat.id
    sec  = data.get("sec")
    page = int(data.get("p", 0))

    section = _SECTIONS.get(sec)
    if not section:
        bot.answer_callback_query(call.id)
        return

    pages = section["pages"]
    total = len(pages)
    page  = max(0, min(page, total - 1))
    pg    = pages[page]
    owner = (uid, cid)

    text = (
        f"{section['emoji']} <b>{section['title']}</b>\n"
        f"{get_lines()}\n\n"
        f"📋 <b>{pg['title']}</b>\n\n"
        f"{pg['content']}"
    )
    if total > 1:
        text += f"\n\n📄 {page+1} / {total}"

    nav = []
    if page < total - 1:
        nav.append(btn("التالي ◀️", "feat_section",
                       {"sec": sec, "p": page+1}, owner=owner, color=_B))
    if page > 0:
        nav.append(btn("▶️ السابق", "feat_section",
                       {"sec": sec, "p": page-1}, owner=owner, color=_B))

    buttons = nav + [
        btn("🔙 القائمة الرئيسية", "feat_back", {}, owner=owner),
        btn("❌ إغلاق",            "feat_close", {}, owner=owner, color=_RD),
    ]
    layout = ([len(nav)] if nav else []) + [2]

    bot.answer_callback_query(call.id)
    edit_ui(call, text=text, buttons=buttons, layout=layout)


@register_action("feat_back")
def on_back(call, data):
    bot.answer_callback_query(call.id)
    _send_main_menu(call.message.chat.id, call.from_user.id, call=call)


@register_action("feat_close")
def on_close(call, data):
    bot.answer_callback_query(call.id)
    try:
        bot.delete_message(call.message.chat.id, call.message.message_id)
    except Exception:
        pass
