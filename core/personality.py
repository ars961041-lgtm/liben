def typing_delay(chat_id: int, delay: float = 0.6):
    """
    يُرسل حالة 'يكتب...' ثم ينتظر delay ثانية.
    آمن للاستخدام في الخيوط.
    """
    try:
        bot.send_chat_action(chat_id, "typing")
    except Exception:
        pass
    time.sleep(max(0.3, min(delay, 1.5)))


def send_with_delay(chat_id: int, text: str, delay: float = 0.6,
                    parse_mode: str = "HTML", reply_to: int = None,
                    markup=None):
    """
    يُرسل رسالة بعد تأخير واقعي مع حالة الكتابة.
    """
    typing_delay(chat_id, delay)
    kwargs = {"parse_mode": parse_mode}
    if reply_to:
        kwargs["reply_to_message_id"] = reply_to
    if markup:
        kwargs["reply_markup"] = markup
    try:
        return bot.send_message(chat_id, text, **kwargs)
    except Exception as e:
        print(f"[Personality] send_with_delay error: {e}")
        return None


def send_with_delay_async(chat_id: int, text: str, delay: float = 0.6,
                          parse_mode: str = "HTML", reply_to: int = None,
                          markup=None):
    """نسخة غير متزامنة — لا تُعيق الخيط الرئيسي"""
    def _run():
        send_with_delay(chat_id, text, delay, parse_mode, reply_to, markup)
    threading.Thread(target=_run, daemon=True).start()
