def reply(category: str, personality: str = None) -> str:
    """يرجع رداً عشوائياً من الفئة المطلوبة"""
    p = personality or PERSONALITY
    pool = _REPLIES.get(p, _REPLIES["elegant"]).get(category, [])
    if not pool:
        pool = _REPLIES["elegant"].get(category, ["✅"])
    return random.choice(pool)


